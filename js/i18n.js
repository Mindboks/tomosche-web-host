// ================================================================
// ■ 言語検出＆取得関数
// ================================================================
const SUPPORTED_LANGS = Object.keys(I18N);

function getCurrentLang() {
    const stored = localStorage.getItem('tomosche_lang');
    if (stored && SUPPORTED_LANGS.includes(stored)) return stored;
    const browserLang = navigator.language || 'ja';
    if (SUPPORTED_LANGS.includes(browserLang)) return browserLang;
    const shortLang = browserLang.split('-')[0];
    if (SUPPORTED_LANGS.includes(shortLang)) return shortLang;
    return 'ja';
}

function t(key) {
    const lang = getCurrentLang();
    const dict = I18N[lang] || I18N['ja'];
    const value = dict[key];
    if (value === undefined) {
        console.warn(`Translation missing: ${key} (${lang})`);
        return key;
    }
    return value;
}

function setLanguage(lang) {
    if (SUPPORTED_LANGS.includes(lang)) {
        localStorage.setItem('tomosche_lang', lang);
        location.reload();
    }
}

function getLanguageName(lang) {
    return I18N[lang]?.language || lang;
}

// ================================================================
// ■ HTMLに言語を適用する関数
// ================================================================
function applyTranslations() {
    const lang = getCurrentLang();
    document.documentElement.lang = lang;
    
    // テキストノードを更新
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        el.textContent = t(key);
    });
    
    // プレースホルダー
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        el.placeholder = t(key);
    });
}
