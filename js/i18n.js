// ============================================================
// 言語検出 & 取得関数
// ============================================================
const SUPPORTED_LANGS = Object.keys(I18N);

function getCurrentLang() {
    const browserLang = navigator.language || 'ja';
    if (SUPPORTED_LANGS.includes(browserLang)) return browserLang;
    const shortLang = browserLang.split('-')[0];
    if (SUPPORTED_LANGS.includes(shortLang)) return shortLang;
    return 'ja';
}

function t(key) {
    const lang = getCurrentLang();
    const dict = I18N[lang] || I18N['ja'];
    let value = dict[key];
    if (value === undefined) {
        // デバッグ用：辞書にないキーはそのまま表示
        console.warn(`Translation missing: ${key}`);
        return key;
    }
    // プレースホルダー置換（%s を引数で置き換え）
    for (let i = 1; i < arguments.length; i++) {
        value = value.replace('%s', arguments[i]);
    }
    return value;
}

function setLanguage(lang) {
    if (SUPPORTED_LANGS.includes(lang)) {
        localStorage.setItem('tomosche_lang', lang);
        location.reload();
    }
}

function getStoredLang() {
    return localStorage.getItem('tomosche_lang') || getCurrentLang();
}

// ============================================================
// 多言語ソート関数（友達一覧用）
// ============================================================
function sortByName(arr, key = 'name') {
    const lang = getStoredLang();
    return arr.sort((a, b) => {
        const aVal = a[key] || '';
        const bVal = b[key] || '';
        return aVal.localeCompare(bVal, lang);
    });
}

// ============================================================
// ページ要素の多言語化（DOM操作）
// ============================================================
function applyTranslations(keys) {
    const lang = getStoredLang();
    keys.forEach(({ id, key, attr = 'textContent' }) => {
        const el = document.getElementById(id);
        if (el) {
            const value = t(key);
            if (attr === 'textContent') {
                el.textContent = value;
            } else if (attr === 'innerHTML') {
                el.innerHTML = value;
            } else if (attr === 'placeholder') {
                el.placeholder = value;
            } else if (attr === 'value') {
                el.value = value;
            }
        }
    });
}
